class ModelSpec:
    """Specification for a model available for distillation."""
    def __init__(self, model_id, source, path, size_gb=0, curricula=None):
        self.model_id = model_id
        self.source = source       # 'ollama' or 'gguf'
        self.path = path           # GGUF file path or Ollama model name
        self.size_gb = size_gb
        self.curricula = curricula or ["communication", "learning", "questioning", "sft_sm_qft", "sft_cosmology", "sft_biochem", "sft_classical_condensed", "sft_tool_use"]
        self.distilled = False     # True when this model is exhausted
        self.iterations = 0        # How many iterations completed
        self.stagnation_score = 0  # Rolling stagnation metric
    
    def __repr__(self):
        return f"ModelSpec({self.model_id}, src={self.source}, curricula={self.curricula})"


class ModelPool:
    """
    Discovers and manages all available models for distillation.
    """
    def __init__(self, ollama_url="http://localhost:11434", llama_server_port=8090):
        self.ollama_url = ollama_url
        self.llama_server_port = llama_server_port
        self.models = []
        self._llama_process = None
        self._active_gguf_model = None
        
    def discover_all(self):
        """Discover all models from Ollama and external SSD."""
        self.models = []
        self._discover_ollama()
        self._discover_gguf()
        distill_logger.info(
            f"ModelPool: discovered {len(self.models)} models total "
            f"({sum(1 for m in self.models if m.source == 'ollama')} ollama, "
            f"{sum(1 for m in self.models if m.source == 'gguf')} gguf)"
        )
        return self.models
    
    def _discover_ollama(self):
        """List all models available in Ollama."""
        try:
            resp = requests.get(f"{self.ollama_url}/api/tags", timeout=5)
            if resp.status_code == 200:
                models = resp.json().get("models", [])
                for m in models:
                    name = m["name"]
                    size_gb = m.get("size", 0) / (1024**3)
                    
                    # Assign curricula based on model name
                    curricula = ["communication", "learning", "questioning", "sft_sm_qft", "sft_cosmology", "sft_biochem", "sft_classical_condensed", "sft_tool_use"]
                    
                    self.models.append(ModelSpec(
                        model_id=name,
                        source="ollama",
                        path=name,
                        size_gb=size_gb,
                        curricula=curricula,
                    ))
                distill_logger.info(f"Discovered {len(models)} Ollama models.")
        except Exception as e:
            distill_logger.error(f"Ollama discovery failed: {e}")
    
    def _discover_gguf(self):
        """Scan external SSD for GGUF model files."""
        search_dirs = [_GGUF_DIR, _HIST_DIR]
        
        for base_dir in search_dirs:
            if not os.path.exists(base_dir):
                continue
            
            for entry in os.listdir(base_dir):
                full_path = os.path.join(base_dir, entry)
                
                if entry.endswith(".gguf") and os.path.isfile(full_path):
                    size_gb = os.path.getsize(full_path) / (1024**3)
                    model_id = entry.replace(".gguf", "")
                    
                    curricula = self._guess_curricula(model_id)
                    
                    self.models.append(ModelSpec(
                        model_id=f"gguf:{model_id}",
                        source="gguf",
                        path=full_path,
                        size_gb=size_gb,
                        curricula=curricula,
                    ))
                
                elif os.path.isdir(full_path):
                    # Multi-part model in a directory
                    gguf_files = [f for f in os.listdir(full_path) if f.endswith(".gguf")]
                    if gguf_files:
                        # Use the first shard
                        shard_path = os.path.join(full_path, sorted(gguf_files)[0])
                        total_size = sum(
                            os.path.getsize(os.path.join(full_path, f))
                            for f in gguf_files
                        ) / (1024**3)
                        
                        curricula = self._guess_curricula(entry)
                        
                        self.models.append(ModelSpec(
                            model_id=f"gguf:{entry}",
                            source="gguf",
                            path=shard_path,
                            size_gb=total_size,
                            curricula=curricula,
                        ))
        
        distill_logger.info(f"Discovered {sum(1 for m in self.models if m.source == 'gguf')} GGUF models.")
    
    def _guess_curricula(self, model_name):
        """Guess appropriate curricula based on model name."""
        name = model_name.lower()
        return ["communication", "learning", "questioning", "sft_sm_qft", "sft_cosmology", "sft_biochem", "sft_classical_condensed", "sft_tool_use"]
    
    def load_for_inference(self, model_spec):
        """
        Prepare a model for inference. Returns the API endpoint to use.
        
        For Ollama models: just returns the ollama URL (already loaded).
        For GGUF models: starts llama-server with the model file.
        """
        if model_spec.source == "ollama":
            return self.ollama_url, model_spec.path
        
        elif model_spec.source == "gguf":
            # Stop any previously running llama-server
            self._stop_llama_server()
            
            distill_logger.info(
                f"Starting llama-server for {model_spec.model_id} "
                f"({model_spec.size_gb:.1f} GB)..."
            )
            
            try:
                cmd = [
                    "llama-server",
                    "-m", model_spec.path,
                    "--port", str(self.llama_server_port),
                    "-ngl", "99",  # Full GPU offload on M3 Ultra
                    "-c", "4096",
                    "--no-mmap",   # Better for external SSD
                ]
                
                self._llama_process = subprocess.Popen(
                    cmd,
                    stdout=subprocess.DEVNULL,
                    stderr=subprocess.DEVNULL,
                )
                self._active_gguf_model = model_spec.model_id
                
                # Wait for server to be ready
                url = f"http://localhost:{self.llama_server_port}"
                for attempt in range(60):  # Up to 60 seconds for large models
                    try:
                        r = requests.get(f"{url}/health", timeout=2)
                        if r.status_code == 200:
                            distill_logger.info(
                                f"llama-server ready for {model_spec.model_id} "
                                f"after {attempt+1}s"
                            )
                            return url, model_spec.model_id
                    except Exception:
                        pass
                    time.sleep(1)
                
                distill_logger.error(
                    f"llama-server failed to start for {model_spec.model_id}"
                )
                self._stop_llama_server()
                return None, None
                
            except Exception as e:
                distill_logger.error(f"Failed to start llama-server: {e}")
                return None, None
    
    def _stop_llama_server(self):
        """Stop any running llama-server instance."""
        if self._llama_process and self._llama_process.poll() is None:
            distill_logger.info(f"Stopping llama-server for {self._active_gguf_model}")
            self._llama_process.terminate()
            try:
                self._llama_process.wait(timeout=10)
            except subprocess.TimeoutExpired:
                self._llama_process.kill()
            self._llama_process = None
            self._active_gguf_model = None
    
    def unload_all(self):
        """Stop all running model servers."""
        self._stop_llama_server()
    
    def get_undistilled(self):
        """Return list of models not yet fully distilled."""
        return [m for m in self.models if not m.distilled]
    
    def summary(self):
        """Return a formatted summary of all models."""
        lines = ["── Model Pool ──────────────────────"]
        for m in self.models:
            status = "✓" if m.distilled else "○"
            lines.append(
                f"  {status} {m.model_id} ({m.source}, {m.size_gb:.1f}GB) "
                f"iter={m.iterations} curricula={m.curricula}"
            )
        lines.append(f"  Total: {len(self.models)} models, "
                     f"{sum(1 for m in self.models if m.distilled)} distilled")
        lines.append("────────────────────────────────────")
        return "\n".join(lines)


