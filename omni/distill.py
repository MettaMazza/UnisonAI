# DISTILLATION ENGINE
# ═══════════════════════════════════════════════════════════════════════════
class DistillationEngine:
    """
    The Learning Law (paper Sec 8.3): the corpus's three-stage arc -- HOLD at the
    prediction state, CLOSE by observation, CONSOLIDATE into the held cycle.
    Grounded in constants/deposition_law.ep (Step 310: the holding store closes
    BEFORE the selecting store), constants/deposition_approach_form.ep (Step 319:
    consolidation is the fold's dyadic halving), and constants/hold_closure_regimes.ep
    (Step 312: held content re-excites, closure does not -- so the engine's OWN
    unconfirmed output is never self-reinforced; only observation-closed content
    consolidates). Cycles through teacher models, each with its own curriculum;
    detects stagnation and auto-switches; the Observer monitors quality throughout.
    """
    def __init__(self, model_pool, teacher=None, stagnation_threshold=0.7,
                 max_stagnation_streak=5, max_iters_per_seed=3):
        self.pool = model_pool
        self.stagnation_threshold = stagnation_threshold
        self.max_stagnation_streak = max_stagnation_streak
        # Per-seed iteration budget: advance to the next seed after this many
        # self-play turns even if mastery never registers, so the curriculum is
        # actually COVERED. Without this the seed only advanced on 5 consecutive
        # near-identical responses (mastery), which varied generation never
        # produces — so self-play got stuck on seed 0 forever.
        self.max_iters_per_seed = max_iters_per_seed
        self.seed_iterations = 0

        # The communication/learning/questioning curriculum is LIVE and teacher-
        # generated — nothing is hardcoded. At start we generate a FRESH batch and
        # merge it onto the persistent cache (fresh + grow + persist); during the
        # loop grow_curricula() keeps adding more.
        self.teacher = teacher
        self._grow_area_idx = 0
        self.pass_completed = False   # set True when a full curriculum pass is mastered
        # Install whatever is CACHED immediately (fast, non-blocking) so the engine
        # is usable at once. The fresh generation (a) is heavy (many blocking HTTP
        # calls) and MUST NOT run here — __init__ is called on the Discord event
        # loop, and blocking it stalls the heartbeat. refresh_all() does the fresh
        # generation, and the auto-loop runs it in a worker thread.
        try:
            self._install(load_cache())
            distill_logger.info(
                "Curriculum loaded from cache: "
                + ", ".join(f"{a}={len(CURRICULA.get(a, []))}" for a in SKILL_AREAS)
            )
        except Exception as e:
            distill_logger.error(f"Could not load cached curricula: {e}", exc_info=True)
        
        # State
        self.current_model_idx = 0
        self.current_curriculum_idx = 0
        self.current_seed_idx = 0
        self.total_iterations = 0
        
        # Stagnation detection per model
        self.recent_responses = deque(maxlen=20)
        self.stagnation_streak = 0
        
        # Progress tracking
        from omni.logging_config import LOG_DIR as _LD
        self.distill_log_path = os.path.join(_LD, "distill.jsonl")
    
    def _install(self, curricula):
        """Inject generated curricula into the global registry used by get_next_seed."""
        for area in SKILL_AREAS:
            if curricula.get(area):
                CURRICULA[area] = curricula[area]

    def refresh_all(self):
        """
        Fresh generation (a): generate a new batch for ALL skill areas, merge into
        the persistent cache, and install. BLOCKING (many HTTP calls) — callers on
        the event loop must run this via asyncio.to_thread so the heartbeat is free.
        Returns {area: new_total} or None.
        """
        if self.teacher is None:
            return None
        merged = refresh_curricula(self.teacher)
        self._install(merged)
        return {a: len(CURRICULA.get(a, [])) for a in SKILL_AREAS}

    def grow_curricula(self):
        """
        Continuous growth (b): generate a fresh batch for ONE rotating skill area,
        merge it into the persistent cache, and install it. One area per call keeps
        the pause short. Returns (area, new_total) or None.
        """
        if self.teacher is None:
            return None
        areas = list(SKILL_AREAS)
        area = areas[self._grow_area_idx % len(areas)]
        self._grow_area_idx += 1
        merged = refresh_curricula(self.teacher, areas=[area])
        self._install(merged)
        return area, len(CURRICULA.get(area, []))

    def curriculum_is_empty(self):
        """True if no teacher-generated curriculum has been installed yet."""
        return not any(CURRICULA.get(a) for a in SKILL_AREAS)

    def curriculum_counts(self):
        """{area: number of installed teacher-generated seeds}."""
        return {a: len(CURRICULA.get(a, [])) for a in SKILL_AREAS}

    def take_pass_completed(self):
        """Return True once if a full curriculum pass has completed since last checked."""
        if self.pass_completed:
            self.pass_completed = False
            return True
        return False

    def get_current_model(self):
        """Get the current model spec to distill from."""
        undistilled = self.pool.get_undistilled()
        if not undistilled:
            return None
        self.current_model_idx = self.current_model_idx % len(undistilled)
        return undistilled[self.current_model_idx]
    
    def get_next_seed(self, model_spec):
        """Get the next curriculum seed for the current model."""
        curricula_names = model_spec.curricula
        curr_name = curricula_names[self.current_curriculum_idx % len(curricula_names)]
        seeds = CURRICULA.get(curr_name) or CURRICULUM_SM_QFT   # never an empty list
        seed = seeds[self.current_seed_idx % len(seeds)]
        return seed, curr_name
    
    def advance(self, response_text):
        """
        Advance the distillation state after processing a response.

        Advances to the next seed on stagnation-mastery OR once the per-seed
        iteration budget is spent (so the curriculum is covered even when
        generation stays varied and mastery never registers as a stagnation streak).

        Returns: 'continue', 'next_seed', 'next_curriculum', 'next_model', or 'complete'
        """
        self.total_iterations += 1
        model = self.get_current_model()
        if model is None:
            self.pass_completed = True   # every model has worked through its curriculum
            return "complete"

        model.iterations += 1
        self.seed_iterations += 1

        mastered = False
        if self._check_stagnation(response_text):
            self.stagnation_streak += 1
            if self.stagnation_streak >= self.max_stagnation_streak:
                mastered = True
        else:
            self.stagnation_streak = 0

        if mastered or self.seed_iterations >= self.max_iters_per_seed:
            return self._advance_seed(model)

        return "continue"

    def _advance_seed(self, model):
        """Move to the next curriculum seed, rolling over the curriculum and then
        the model when the current one's seeds are exhausted. Resets per-seed
        state. Returns the transition: 'next_seed' | 'next_curriculum' | 'next_model'."""
        self.current_seed_idx += 1
        self.stagnation_streak = 0
        self.seed_iterations = 0
        self.recent_responses.clear()

        curricula_names = model.curricula
        curr_name = curricula_names[self.current_curriculum_idx % len(curricula_names)]
        seeds = CURRICULA.get(curr_name, CURRICULUM_SM_QFT)

        if self.current_seed_idx >= len(seeds):
            self.current_curriculum_idx += 1
            self.current_seed_idx = 0
            if self.current_curriculum_idx >= len(curricula_names):
                model.distilled = True
                self.current_model_idx += 1
                self.current_curriculum_idx = 0
                self.pass_completed = True   # this model finished its whole curriculum
                return "next_model"
            return "next_curriculum"
        return "next_seed"
    
    def _check_stagnation(self, response_text):
        """Check if the latest response is too similar to recent ones."""
        # Use word sets for similarity to avoid positional/shifting brittleness
        words = set(response_text.lower().split())
        
        if len(self.recent_responses) < 5:
            self.recent_responses.append(words)
            return False
        
        # Check semantic/word-level overlap with recent responses
        overlap_count = 0
        for prev_words in self.recent_responses:
            if not prev_words or not words:
                continue
                
            intersection = len(words.intersection(prev_words))
            union = len(words.union(prev_words))
            ratio = intersection / union if union > 0 else 0
            
            if ratio > self.stagnation_threshold:
                overlap_count += 1
        
        self.recent_responses.append(words)
        
        # Stagnant if >50% of recent responses are >70% similar
        return overlap_count > len(self.recent_responses) * 0.5
    
    def query_model(self, api_url, model_name, prompt, system=None, temperature=0.9,
                    top_p=0.95, repeat_penalty=1.2):
        """
        Query a model via Ollama or llama-server compatible API.
        
        Returns the response text.
        """
        try:
            if "localhost:11434" in api_url:
                # Ollama API
                resp = requests.post(
                    f"{api_url}/api/generate",
                    json={
                        "model": model_name,
                        "prompt": prompt,
                        "system": system if system else "",
                        "stream": False,
                        "options": {
                            "num_predict": 8192,
                            "temperature": temperature,
                            "top_p": top_p,
                            "repeat_penalty": repeat_penalty,
                            "num_ctx": detect_context_window(model_name, api_url)  # provider-reported full window
                        },
                    },
                    timeout=120,
                )
                if resp.status_code == 200:
                    return resp.json().get("response", "")
            else:
                # llama-server API (OpenAI-compatible)
                resp = requests.post(
                    f"{api_url}/v1/completions",
                    json={
                        "prompt": prompt,
                        "max_tokens": 8192,
                        "temperature": temperature,
                        "top_p": top_p,
                        "repeat_penalty": repeat_penalty,
                    },
                    timeout=120,
                )
                if resp.status_code == 200:
                    choices = resp.json().get("choices", [])
                    if choices:
                        return choices[0].get("text", "")
            
            return ""
            
        except Exception as e:
            distill_logger.error(f"Query failed: {e}")
            return ""
    
    def log_iteration(self, model_id, curriculum, seed, response, 
                      self_rating=None, teacher_rating=None, observer_note=None):
        """Log a distillation iteration to the JSONL log."""
        import datetime
        record = {
            "timestamp": datetime.datetime.utcnow().isoformat() + "Z",
            "iteration": self.total_iterations,
            "model": model_id,
            "curriculum": curriculum,
            "seed": seed,
            "response_length": len(response),
            "response_preview": response[:200],
            "self_rating": self_rating,
            "teacher_rating": teacher_rating,
            "observer_note": observer_note,
        }
        try:
            with open(self.distill_log_path, "a") as f:
                f.write(json.dumps(record) + "\n")
        except Exception:
            distill_logger.error("Failed to write distill log", exc_info=True)
    
    def status(self):
        """Return a status summary."""
        model = self.get_current_model()
        model_name = model.model_id if model else "COMPLETE"
        undistilled = len(self.pool.get_undistilled())
        total = len(self.pool.models)
        
        return {
            "total_iterations": self.total_iterations,
            "current_model": model_name,
            "models_remaining": undistilled,
            "models_total": total,
            "current_curriculum_idx": self.current_curriculum_idx,
            "current_seed_idx": self.current_seed_idx,
            "stagnation_streak": self.stagnation_streak,
        }
